package AbcTechProgramms;

public class SwappingString {

	public static void main(String[] args) {
      String s1="Venkata";
      String s2="Hemanth";
      String temp="";
      
      temp=s1;
      s1=s2;
      s2=temp;
      System.out.println("After Swapping "+s1);
      System.out.println("After Swapping "+s2);
      
      
	}

}
